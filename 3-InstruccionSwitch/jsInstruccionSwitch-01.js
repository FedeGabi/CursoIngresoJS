function mostrar()
{
	//tomo el mes
	var mes;
	
	mes = document.getElementById("txtIdMes").value;

switch(mes){
			case "Enero":
				alert("Que comiences bien el año")
				break;

			case "Marzo":
					alert("A clases")
				break;


			case "Julio":
					alert("Se vienen las vacaciones")
				break;


			case "Diciembre":
					alert("¡Felices fiestas!")
				break;


} //la funcion Switch funciona con igualdades, no con valores de true o false como en la sentencia if.

	
	
	




}//FIN DE LA FUNCIÓN